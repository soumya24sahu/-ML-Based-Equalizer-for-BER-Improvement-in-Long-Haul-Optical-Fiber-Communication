module SingleLayer  #(
    parameter NUM_SAMPLES  = 10,   // Number of complex input samples
    parameter SIGNAL_WIDTH = 10,   // 10-bit fixed-point (1.9 format)
    parameter KERNEL_WIDTH = 8,    // Kernel coefficient width
    parameter DATA_WIDTH   = 10,   // Data width for KerrNonlinearityTaylor
    parameter L_EFF        = 5,    // Effective nonlinear length
    parameter GAMMA        = -13,   // ? scaled by 10 (for KerrNonlinearityTaylor)
    parameter KERNEL_SIZE = 6
)(
    input clk,       // Clock signal
    input reset,     // Reset signal
    input enable,    // Enable processing
    input prev_done,

    input  [2*SIGNAL_WIDTH*NUM_SAMPLES-1:0] in,    // Flattened complex inputs: [real0, imag0, ..., real9, imag9]
    input  [KERNEL_SIZE*KERNEL_WIDTH-1:0] kernel,           // Symmetric kernel: [k0_real, k1_real, k0_imag, k1_imag]

    output [2*SIGNAL_WIDTH*NUM_SAMPLES-1:0] out,  // Flattened complex outputs
    output done   // Processing completion flag
);

// ????????????????????????????????????????????????????????????
// ?                    Internal Signals                     ?
// ????????????????????????????????????????????????????????????

wire [2*SIGNAL_WIDTH*NUM_SAMPLES-1:0] conv_out; // Output from convolution layer
wire conv_done; // Done signal from convolution layer

// ????????????????????????????????????????????????????????????
// ?                    Convolution Layer                    ?
// ????????????????????????????????????????????????????????????

AdvComplexConvLayer #(
    .NUM_SAMPLES(NUM_SAMPLES),
    .SIGNAL_WIDTH(SIGNAL_WIDTH),
    .KERNEL_WIDTH(KERNEL_WIDTH),
    .KERNEL_SIZE(KERNEL_SIZE)
) conv_layer (
    .clk(clk),
    .reset(reset),
    .enable(enable),
    .in(in),
    .kernel(kernel),
    .out(conv_out),
    .done(conv_done)
);

// ????????????????????????????????????????????????????????????
// ?               Kerr Nonlinearity Processing               ?
// ????????????????????????????????????????????????????????????

genvar i;
generate
    for (i = 0; i < NUM_SAMPLES; i = i + 1) begin : NONLINEARITY_GEN
        // Extract real & imaginary parts from convolution output
        wire signed [SIGNAL_WIDTH-1:0] real_in = conv_out[2*SIGNAL_WIDTH*i +: SIGNAL_WIDTH];
        wire signed [SIGNAL_WIDTH-1:0] imag_in = conv_out[2*SIGNAL_WIDTH*i + SIGNAL_WIDTH +: SIGNAL_WIDTH];

        // Nonlinearity module outputs
        wire signed [SIGNAL_WIDTH-1:0] real_out;
        wire signed [SIGNAL_WIDTH-1:0] imag_out;

        // Instantiate Kerr Nonlinearity Taylor Expansion
        KerrNonlinearityTaylor #(
            .DATA_WIDTH(DATA_WIDTH),
            .L_EFF(L_EFF),
            .GAMMA(GAMMA)
        ) nonlinearity (
            .real_in(real_in),
            .imag_in(imag_in),
            .real_out(real_out),
            .imag_out(imag_out)
        );

        // Assign outputs to final result array
        assign out[2*SIGNAL_WIDTH*i +: SIGNAL_WIDTH] = real_out;
        assign out[2*SIGNAL_WIDTH*i + SIGNAL_WIDTH +: SIGNAL_WIDTH] = imag_out;
    end
endgenerate

// ????????????????????????????????????????????????????????????
// ?                     Done Signal Logic                    ?
// ????????????????????????????????????????????????????????????

assign done = conv_done & prev_done;

endmodule
